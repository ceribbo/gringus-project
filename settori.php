                Infissi ad Alto Contenuto Energetico

                Secondo l’ENEA più del 45% del fabbisogno energetico nazionale è dovuto al riscaldamento invernale e al raffreddamento estivo delle abitazioni.
                Gli infissi possono essere la causa maggiore di dispersione di energia termica ma, contrariamente a quanto si pensa comunemente, la vera dispersione dovuta agli spifferi d'aria avviene tramite il vetro.
                Lo sviluppo di nuove tecnologie negli ultimi anni ha investito questo settore rendendo disponibili sul mercato infissi fino ad alcuni anni fa inimmaginabili.

                Vantaggi
                -	Riduzione del flusso di calore uscente in inverno;
                -	Riduzione del flusso di calore entrante in estate;
                -	Eliminazione del rischio condensa sul vetro interno e sul telaio.
                -	Riduzione dei moti convettivi di aria che tendono a raffreddare e umidificare il vetro nel periodo invernale. 
                -	Miglioramento dell’isolamento acustico (in relazione al tipo di infisso e di vetro)

                Per i motivi sopra citati, l’adozione di infissi ad elevate prestazioni termiche comporta un innalzamento della temperatura superficiale della parete con conseguenti benefici in termini di comfort e di risparmio energetico.


                Svantaggi

                Raggiunti certi livelli di prestazioni, l’incremento del costo degli infissi comporta tempi di ammortamento piuttosto lunghi, per cui l’investimento non risulta più essere appetibile dal punto di vista puramente economico. 
                Va comunque sottolineato che in tal caso le migliori prestazioni termiche si traducono comunque in un incremento delle condizioni di comfort. 
                Per tale motivo, soprattutto in caso di condizioni invernali impegnative (come temperature o durata della stagione fredda) e/o superfici vetrate di dimensioni considerevoli, l’adozione di infissi ad elevate prestazioni è comunque auspicabile.


                Possiamo dunque dire che l’adozione di infissi ad elevate prestazioni termiche, oltre a comportare una riduzione delle dispersioni e ad offrire la possibilità di un maggior sfruttamento degli apporti energetici solari gratuiti, garantisce un notevole incremento del Comfort Termico all’interno dell’edificio.

