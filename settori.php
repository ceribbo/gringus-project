                -	Secondo la Commissione Europea si stima un risparmio annuo di 50 euro a famiglia con l’utilizzo ottimale di questa tecnologia.


                Nota negativa: Contengono una piccola quantità di mercurio, elemento che deve essere smaltito in maniera specifica. una volta esaurite, devono essere portate presso i punti riciclaggio della propria città o dove sono state acquistate.

