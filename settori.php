                <a name="pompe">Pompe Di Calore </a>

                Questi climatizzatori di ultima generazione permettono di sostituire efficacemente i tradizionali impianti di riscaldamento e di raffrescamento attraverso il principio dell’aria e dell’acqua, il vantaggio di due sorgenti di energia disponibili ovunque.


                Ma Che cos’è una pompa di calore? È una macchina che produce energia termica sfruttando sorgenti esterne, come aria, acqua o suolo. L’energia prodotta può essere utilizzata per riscaldare, raffrescare e per produrre acqua calda. Si parla di impianto geotermico, il più diffuso tra le applicazioni della pompa di calore, quando la sorgente di scambio termico è il terreno.


                Tipologie 

                Esistono 4 tipologie di Pompe di Calore, ognuna più adatta a seconda del luogo e le caratteristiche che accomunano i vostri immobili (Le dividiamo ognuna in un’apertura web. Ci clicchi sopra e appare la didascalia annessa)

                1)	Aria- Aria: 
                La più diffusa ed economica sul mercato, preleva calore dalla sorgente fredda costituita dall’aria esterna e la cede al pozzo caldo costituito dall’aria dell’ambiente da riscaldare. 
                Sono molto semplici da installare e non richiedono modifiche importanti agli impianti preesistenti.

                2)	Aria-Acqua
                In pratica preleva calore dalla sorgente fredda costituita dall’aria esterna e la cede al pozzo caldo costituito da un circuito d’acqua per riscaldare l’ambiente.
                In estate dunque produce acqua fredda che abbassa la temperatura dei locali. D’inverno invece, per processo inverso, riscalda l’ambiente (in più acqua calda tutto l’anno).

                3)	Acqua-Acqua
                Preleva calore dalla sorgente fredda costituita dall’acqua (di falda, lago, fiume o mare) e la cede al pozzo caldo costituito da un circuito d’acqua di riscaldamento degli ambienti. (Fondamentale è conoscere e rispettare i regolamenti locali che stabiliscono la possibilità e le modalità di usufruire dell'acqua di falda.)

                4)	Terreno-Acqua
                Preleva calore dalla sorgente fredda costituita dal terreno e la cede al pozzo caldo costituito da un circuito d'acqua di riscaldamento

                Vantaggi/Svantaggi

                L’installazione di una pompa di Calore conviene poiché i Vantaggi sono sicuramente superiori agli Svantaggi ed è sicuramente una scelta ‘eco’, ma deve dipendere da una valutazione tecnica approfondita di tutti gli aspetti in gioco: potenzialità dell’impianto, abbinamenti con altre tecnologie etc
                (Metterei Vantaggi e Svantaggi con un link di connessione o una didascalia accanto per ricordarli ma senza troppa foga)

                Vantaggi
                -	Rendimento Energetico elevato
                -	Conveniente sul piano economico (poiché gas costa maggiormente che elettricità)
                -	Risparmio in bolletta di circa il 40% della spesa totale (se abbinata ad un impianto di riscaldamento e raffrescamento radiante garantisce un risparmio energetico dal 40% al 70% rispetto ai sistemi tradizionali.)
                -	Ambientalmente parlando, incrementa l’utilizzo di energia rinnovabile e in questo modo riduce le emissioni climalteranti.

                Svantaggi
                -	Il Freddo: temperature invernali molto rigide (sotto gli 0°C) influiscono negativamente sulle prestazioni delle pompe di calore (apparte le Acqua-Acqua).
                -	La macchina può dare rumori fastidiosi ed essere ingombrante (meglio posizionarla all’aperto).
                -	Un impianto Aria-Acqua è più complesso (dunque più costoso) degli altri impianti.

