                OLED (Organic Light Emitting Diode)

                Gemello della tecnologia LED con un “Organic” in più, ma che può fare la differenza. 
                Si costituiscono da più stati organici a differenza dei semi-conduttori delle lampade a LED che invece sono inorganici il che permettono:
                -	Una maggior efficienza rispetto ai LED (anche se minima)
                -	Elevata efficienza in termini di consumo (da cui un minor dispendio energetico)
                -	Privi al 100% di raggi ultravioletti, mercurio e ultrarossi (vedi LED).
                CFL (Compact Fluorescent Lamp)

                Le tipiche lampadine a spirale con una durata fino a 8-10 anni, molto superiori rispetto alle vecchie tecniche di illuminazione (lampadina a incandescenza; Tubi al Neon, ai vapori di Mercurio) a cui ci avevano abituato.

