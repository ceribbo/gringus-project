                Isolamento Termico Muri (Coibentazione)

                Oggigiorno Isolare le pareti di un edificio può essere un investimento duraturo e importante, sia per garantire un buon risparmio che dal punto di vista energetico. Per farlo, si hanno a disposizione diverse opportunità, la cui scelta dipende dai materiali utilizzati per la costruzione dell'abitazione e dal tipo di immobile.

                Esaminiamo queste possibilità per garantirvi la miglior scelta possibile a seconda di ciò che desiderate:

                -	Cappotto Esterno 
                L'isolamento a cappotto esterno è sicuramente tra i metodi più efficaci. Considerata la costruzione originale, applicate, sulla sezione esterna delle pareti, dei pannelli speciali (in lana minerale e polistirene espanso) in grado di essere isolanti. Lo spessore dei pannelli parte dai 6 cm e risulta essere la scelta migliore per chi vive in zone in cui la differenza di temperatura tra il giorno e la notte è molto amplia.

                -	Cappotto Interno
                Per gli edifici che non sono sottoposti ad un riscaldamento continuo, viene preferito il cappotto interno, poiché la diffusione del calore è più immediata. In questo caso agirete sull'interno delle pareti mediante delle contro-pareti formate da pannelli composti. C'è da dire che tra tutte le modalità per isolare i muri, questa è l'unica attuabile completamente da soli, senza quindi la necessità di personale specializzato. I pannelli, infatti, vengono venduti già pronti per l'installazione.

                -	Riempimento Dell’Intercapedine
                L'intervento più utilizzato e certamente uno dei più efficaci. Come dice il nome stesso, si tratta di riempire l'intercapedine centrale dei muri con degli appositi isolanti. A tale scopo, bisognerà praticare dei fori nelle pareti (distanti circa 2 metri l'uno dall'altro) e, tramite essi, procedere al riempimento utilizzando materiale sciolto (come sughero o argilla espansa) oppure materiale inizialmente liquido (come ad esempio la resina).  

                -	Isolamento tramite pareti verticali

                Viene attuato per gli immobili ancora in fase di costruzione. Oltre all'isolamento termico, viene garantito anche un ottimo isolamento acustico. Sono due i pannelli che costituiscono i muri perimetrali: uno esterno, più spesso, ed uno interno, meno spesso. Tra di essi è presente una camera d'aria di circa 4 cm riempita con della lana di roccia, per garantire un perfetto isolamento. 
                Prima di procedere al lavoro bisogna verificare se esistono restrizioni comunali sui muri esterni.

                Vantaggi

                -	Pareti del muro maggiormente protette da escursioni termiche.
                -	La muratura resta più calda d’inverno e più fresca in estate (miglior confort abitativo)
                -	Evita la condensa dell’umidità (da cui la conseguente formazione di muffa).
                -	Elimina o Corregge i ponti termici (solai, travi e pilastri), principali porte per il freddo nell’immobile.
                -	Abbatte l’inquinamento acustico in maniera drastica e previene crepe o spaccature

                Per quanto riguarda i cappotti, vengono realizzati attraverso l’applicazione a colla e/o tassellatura di pannelli combinati con i più svariati materiali siano essi di origine naturale (sughero, silicato di calcio, fibra di legno solo per citarne alcuni), che sottolineano l’importanza sostenibile nel loro utilizzo.

                Esistono anche cappotti realizzati con prodotti derivati dal ciclo degli idrocarburi (polistirolo, stirene, poliuretano), che nonostante un coefficiente superiore di resistenza al passaggio del calore, presentano varie problematiche:

                -	Scarsa permeabilità al passaggio del vapore (pertanto non vi è traspirazione tra l’interno e l’esterno dell’edificio)
                -	Difficoltà nei costi per lo smaltimento in caso di demolizione.
                -	Maggior consumo di anidride carbonica per la realizzazione.


                Vantaggi Economici

                -	Aumento del valore immobiliare secondo il tipo di realizzazione.
                -	Sgravi Fiscali del 55% previsti dalla vigente finanziaria (fino al 31 Dicembre 2015→65%)
